public class Armor extends Equipment{
    private int defPower;
    private int effect;

    public int getDefPower() {
        return defPower;
    }

    public void setDefPower(int defPower) {
        this.defPower = defPower;
    }

    public int getEffect() {
        return effect;
    }

    public void setEffect(int effect) {
        this.effect = effect;
    }


}
